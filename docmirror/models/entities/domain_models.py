class DomainData(BaseModel):
    """
    Domain-specific data wrapper context.

    Populated by selectively mapping into a corresponding nested schema.
    Typically only one corresponding subfield remains exclusively valid.
    """

    document_type: str = "other"

    bank_statement: BankStatementData | None = None
    invoice: InvoiceData | None = None
    tax_report: TaxReportData | None = None
    business_license: BusinessLicenseData | None = None
    contract: ContractData | None = None
    financial_report: FinancialReportData | None = None
    id_card: IDCardData | None = None

    @property
    def active_model(self) -> BaseModel | None:
        """Returns the actively configured explicitly defined domain."""
        for field_name in [
            "bank_statement",
            "invoice",
            "tax_report",
            "business_license",
            "contract",
            "financial_report",
            "id_card",
        ]:
            val = getattr(self, field_name, None)
            if val is not None:
                return val
        return None
